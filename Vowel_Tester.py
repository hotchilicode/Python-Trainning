# Write a Python program to test whether a passed letter is a vowel or not. 

def vowel_checker(char):
    vawels = ["a", "b", "c" , "d"]
    if char in vawels:
        return 'This is a Vowel'
    else:
        return 'This is not a Vawel'
    

value =  vowel_checker('b')
print(value)

